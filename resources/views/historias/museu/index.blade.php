					<p>
						Por ocasião das  das comemorações do 1-º aniversário da criação do Metical – a moeda nacional – foi inaugurado a 15 de Junho de 1981 o Museu Nacional da Moeda.
					</p>
					<h5>Como surgiu o Museu Nacional da Moeda?</h5>
					<p>
						O Arquivo Histórico de Moçambique (AHM) tinha expostas 399 moedas de Moçambique no Museu de História de Ocupação colonial, na altura designado Museu Histórico Militar da Fortaleza Nossa Senhora da Conceição, hoje fortaleza de Maputo.
					</p>
					<p>
						Esta colecção tinha sido organizada pelo Sr. Caetano Montez e era referente à moeda moçambicana, cunhada desde 1752.
					</p>

					<p>
						Ao assumir a direcção do AHM, o Sr. Dr. Alexandre Lobato deu continuidade ao coleccionamento. A colecção foi-se enriquecendo com moedas moçambicanas, estrangeiras, sobretudo de Portugal, e a 30 de Junho de 1970 foi inaugurado o Gabinete Numismático. O Gabinete, localizado no Rés do Chão do edifício da Biblioteca Nacional, compreendia colecções, bastante expressivas, de moedas, medalhas, cédulas e notas bancárias de Moçambique e outros países e incluindo também alguma bibliografia.
					</p>

					<p>
						O AHM participou em alguns certames numismáticos. Por ocasião das comemorações do dia 25 de Junho de 1975 – Dia da Independência Nacional, o AHMO foi galardoado com uma placa na 1-ª Exposição Nacional de Numismática e Medalhística patrocinada pelo Banco de Moçambique e Associação dos Empregados do Banco de Moçambique em Lourenço Marques.
					</p>
						
					<p>
						A criação de um Museu da Moeda, com edifício próprio, fazia-se sentir. Em 1972 o edifício sito da esquina de Av.25 de Setembro com a Rua da Imprensa, entre a Biblioteca Nacional e a Imprensa Nacional, tinha sido destinado ao Museu. A ideia, porém, não se concretizou.
					</p>

					<p>
						Em 1977, a nova direcção do Arquivo pugna também pela criação de um Museu da Moeda. Após várias diligências é-nos cedida a Casa Amarela para esse efeito.
					</p>

					<p>
						A utilização de Monumentos Históricos para Museus, exposições ou outras actividades de carácter cultural é prática corrente em museologia. A Casa Amarela é um documento histórico. Ela testemunha um estilo arquitectónico corrente no século XIX no então Lourenço Marques. Várias vezes remodelada, a Casa que hoje é o Museu Nacional da Moeda foi residência do Governador e secretária do Governo, mais tarde sede das Repartições Distritais, Secretária Geral, Administração Civil.
					</p>

					<p>
						O Museu Nacional da Moeda abriu as suas portas ao público no dia 20 de Junho de 1981.
					</p>

					<p>
						A inauguração do primeiro museu da moeda insere-se no âmbito da preservação e valorização do património histórico de Moçambique. A moeda é, com efeito, um testemunho histórico de extrema importância para a história de um povo. Ela, a moeda, revela o grau de desenvolvimento económico das sociedades, documenta as relações socioeconómicas, políticas e culturais entre os povos ou países. Inclusivamente, a moeda veicula a ideologia do país que a emite.
					</p>
